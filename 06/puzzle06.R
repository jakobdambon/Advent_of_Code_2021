growth <- function(day, fish_start) {
  # initialize counter
  cnt <- numeric(9)
  names(cnt) <- 0:8
  t <- table(fish_start)
  for (d in names(t)) {
    cnt[d] <- cnt[d] + t[d]
  }
  # loop over number of days
  for (i in 1:day) {
    n0 <- cnt["0"]
    cnt[1:8] <- cnt[2:9]
    cnt[9] <- n0
    cnt["6"] <- cnt["6"] + n0
  }
  cnt
}


